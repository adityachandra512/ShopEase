// Function to simulate API requests with mock data
export const mockAPI = {
  getProducts: (queryParams = {}) => {
    return new Promise((resolve) => {
      setTimeout(() => {
        // Apply filters to mock products
        let filteredProducts = [...mockProducts];
        
        // Filter by search query
        if (queryParams.search) {
          const searchLower = queryParams.search.toLowerCase();
          filteredProducts = filteredProducts.filter(
            product => 
              product.Name.toLowerCase().includes(searchLower) || 
              product.Description.toLowerCase().includes(searchLower)
          );
        }
        
        // Filter by category
        if (queryParams.category) {
          const categories = queryParams.category.split(',');
          filteredProducts = filteredProducts.filter(
            product => categories.includes(product.Category)
          );
        }
        
        // Filter by brand
        if (queryParams.brand) {
          const brands = queryParams.brand.split(',');
          filteredProducts = filteredProducts.filter(
            product => brands.includes(product.Brand)
          );
        }
        
        // Filter by price range
        if (queryParams.minPrice) {
          filteredProducts = filteredProducts.filter(
            product => product.Price >= parseFloat(queryParams.minPrice)
          );
        }
        if (queryParams.maxPrice) {
          filteredProducts = filteredProducts.filter(
            product => product.Price <= parseFloat(queryParams.maxPrice)
          );
        }
        
        // Filter by rating
        if (queryParams.rating) {
          filteredProducts = filteredProducts.filter(
            product => product.Ratings.average >= parseFloat(queryParams.rating)
          );
        }
        
        // Filter by stock
        if (queryParams.inStock === 'true') {
          filteredProducts = filteredProducts.filter(
            product => product.Stock > 0
          );
        }
        
        // Filter by discount
        if (queryParams.discount === 'true') {
          filteredProducts = filteredProducts.filter(
            product => product.Discount > 0
          );
        }
        
        // Sort products
        if (queryParams.sort) {
          switch(queryParams.sort) {
            case 'price_asc':
              filteredProducts.sort((a, b) => a.Price - b.Price);
              break;
            case 'price_desc':
              filteredProducts.sort((a, b) => b.Price - a.Price);
              break;
            case 'rating':
              filteredProducts.sort((a, b) => b.Ratings.average - a.Ratings.average);
              break;
            case 'name_asc':
              filteredProducts.sort((a, b) => a.Name.localeCompare(b.Name));
              break;
            case 'name_desc':
              filteredProducts.sort((a, b) => b.Name.localeCompare(a.Name));
              break;
            case 'discount':
              filteredProducts.sort((a, b) => b.Discount - a.Discount);
              break;
            case 'featured':
              filteredProducts.sort((a, b) => (b.Featured ? 1 : 0) - (a.Featured ? 1 : 0));
              break;
            default:
              break;
          }
        }
        
        // Apply pagination
        const page = parseInt(queryParams.page) || 1;
        const limit = parseInt(queryParams.limit) || 12;
        const startIndex = (page - 1) * limit;
        const endIndex = startIndex + limit;
        const paginatedProducts = filteredProducts.slice(startIndex, endIndex);
        
        resolve({
          products: paginatedProducts,
          pagination: {
            total: filteredProducts.length,
            page: page,
            limit: limit,
            totalPages: Math.ceil(filteredProducts.length / limit)
          }
        });
      }, 500); // simulate network delay
    });
  },
  
  getCategories: () => {
    return new Promise((resolve) => {
      setTimeout(() => {
        resolve(mockCategories);
      }, 300);
    });
  },
  
  getBrands: () => {
    return new Promise((resolve) => {
      setTimeout(() => {
        resolve(mockBrands);
      }, 300);
    });
  },
  
  getTags: () => {
    return new Promise((resolve) => {
      setTimeout(() => {
        const allTags = mockProducts
          .flatMap(product => product.Tags)
          .filter((value, index, self) => self.indexOf(value) === index);
        resolve(allTags);
      }, 300);
    });
  }
};
